#include "libft.h"

void	ft_bzero(void *str, size_t nbr)
{
	unsigned char	*a;
	size_t			i;

	a = (unsigned char *)str;
	i = 0;
	while (nbr > i)
	{
		a[i] = 0;
		i++;
	}
}
