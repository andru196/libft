#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 == s2)
		return (1);
	if (!s1 || !s2)
		return (0);
	if (!n)
		return (1);
	while (*s1 && *s2 && *s1 == *s2 && n--)
	{
		s1++;
		s2++;
	}
	if (!n)
	{
		s1--;
		s2--;
	}
	return (*s1 == *s2 ? 1 : 0);
}
